function randomOurGamesReleases() {
	const ourGamesContainer = document.getElementById("latest-releases");
	if (ourGamesContainer) {
		ourGamesContainer.innerHTML = ourGames[Math.floor(Math.random() * ourGames.length)];
	}
}

function randomHostedGamesReleases() {
	const hostedGamesContainer = document.getElementById("hosted-latest-releases");
	if (hostedGamesContainer) {
		hostedGamesContainer.innerHTML = hostedGames[Math.floor(Math.random() * hostedGames.length)];
	}
}

randomOurGamesReleases();
window.setInterval(randomOurGamesReleases, 4000);
randomHostedGamesReleases();
